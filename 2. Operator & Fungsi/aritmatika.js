let bil1 = 10;
let bil2 = 3;

//Operator Penjumlahan
let jumlah;
jumlah = bil1 + bil2;
console.log(jumlah);

//Operator Pengurangan
let kurang;
kurang = bil1 - bil2;
console.log(kurang);

//Operator Perkalian
let kali;
kali = bil1 * bil2;
console.log(kali);

//Operator Pembagian
let bagi;
bagi = bil1 / bil2;
console.log(bagi);

//Eksponen (Pangkat)
let pangkat;
pangkat = bil1 ** bil2;
console.log(pangkat);

//Modulus (Sisa Hasil Bagi)
let modulus;
modulus = bil1 % bil2;
console.log(modulus);

//Increment | Decrement
bil1++;
console.log(bil1);
bil2--;
console.log(bil2);

